import { toast } from "react-toastify";

export const notifyMintSuccess = () => {
  toast.success("You have successfully minted your NFT", {
    position: "top-right",
    autoClose: 5000,
    hideProgressBar: false,
    closeOnClick: true,
    pauseOnHover: true,
    draggable: true,
    progress: undefined,
  });
};

